var DIMENSION_FALLBACK = { w: 1600, h: 1200 };
var dimensionsCache = {};

function getDimensionsForItem(item) {
	if (item.w && item.h) {
		return Promise.resolve(item);
	}

	var cacheKey = item.src;
	if (dimensionsCache[cacheKey]) {
		item.w = dimensionsCache[cacheKey].w;
		item.h = dimensionsCache[cacheKey].h;
		return Promise.resolve(item);
	}

	return new Promise(function(resolve) {
		var img = new Image();
		img.onload = function() {
			var resolvedWidth = img.naturalWidth || DIMENSION_FALLBACK.w;
			var resolvedHeight = img.naturalHeight || DIMENSION_FALLBACK.h;
			dimensionsCache[cacheKey] = { w: resolvedWidth, h: resolvedHeight };
			item.w = resolvedWidth;
			item.h = resolvedHeight;
			resolve(item);
		};
		img.onerror = function() {
			dimensionsCache[cacheKey] = { w: DIMENSION_FALLBACK.w, h: DIMENSION_FALLBACK.h };
			item.w = DIMENSION_FALLBACK.w;
			item.h = DIMENSION_FALLBACK.h;
			resolve(item);
		};
		img.src = item.src;
	});
}

function resolveDimensions(itemsSubset) {
	return Promise.all(itemsSubset.map(getDimensionsForItem));
}

